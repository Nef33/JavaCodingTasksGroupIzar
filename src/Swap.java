public class Swap {
    public static void main(String[] args) {

        int a = 19;
        int b = 10;

        a = a + b; // 19+10=29

        b = a - b; // 29-10=19

        a = a - b; // 29-19=10

        System.out.println("a = " + a);
        System.out.println("b = " + b);



    }

}


